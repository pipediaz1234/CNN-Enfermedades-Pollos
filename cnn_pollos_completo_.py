print(f'Imagenes encontradas: {df["existe"].sum()} / {len(df)}')
print(f'Imagenes faltantes:   {(~df["existe"]).sum()}')

df = df[df['existe']].reset_index(drop=True)

df_train, df_val = train_test_split(
    df, test_size=0.2, random_state=SEMILLA, stratify=df['label']
)

print(f'Entrenamiento: {len(df_train)} imagenes')
print(f'Validacion:    {len(df_val)} imagenes')
print()
print('Distribucion en entrenamiento:')
