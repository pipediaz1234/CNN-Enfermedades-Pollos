"""---
## PASO 7 — Preprocesamiento y Data Augmentation

**Preprocesamiento:** normalizar pixels de [0-255] a [0-1] y redimensionar a 224x224.

**Data Augmentation:** aplicar transformaciones aleatorias para aumentar variedad y reducir overfitting:
rotaciones, desplazamientos, volteo horizontal, zoom y corte.

"""

IMG_SIZE   = (224, 224)
BATCH_SIZE = 32
EPOCHS     = 30
LR         = 0.001

# Generador de entrenamiento CON augmentation
datagen_train = ImageDataGenerator(
    rescale=1./255,
    rotation_range=20,
    width_shift_range=0.2,
    height_shift_range=0.2,
    horizontal_flip=True,
    zoom_range=0.2,
    shear_range=0.1,
    fill_mode='nearest'
)

# Generador de validacion SIN augmentation
datagen_val = ImageDataGenerator(rescale=1./255)

train_gen = datagen_train.flow_from_dataframe(
    dataframe=df_train,
    x_col='ruta',
    y_col='label',
    target_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='categorical',
    shuffle=True
)

val_gen = datagen_val.flow_from_dataframe(
    dataframe=df_val,
    x_col='ruta',
    y_col='label',
    target_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='categorical',
    shuffle=False
)

CLASES         = list(train_gen.class_indices.keys())
INDICE_A_CLASE = {v: k for k, v in train_gen.class_indices.items()}
NUM_CLASES     = len(CLASES)

print('Clases:', CLASES)
