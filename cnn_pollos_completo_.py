"""---
## PASO 6 — Preparacion del dataset

- Construimos la ruta completa de cada imagen
- Dividimos en 80% entrenamiento y 20% validacion
- `stratify` mantiene la proporcion de clases en ambos conjuntos

"""

IMG_DIR = 'Train'

df['ruta'] = df['images'].apply(lambda nombre: os.path.join(IMG_DIR, nombre))
