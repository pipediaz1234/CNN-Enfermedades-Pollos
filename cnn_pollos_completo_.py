axes[1].plot(epocas, hist['loss'],     'b-o', label='Train', markersize=4)
axes[1].plot(epocas, hist['val_loss'], 'r-o', label='Val',   markersize=4)
axes[1].set_title('Loss')
axes[1].set_xlabel('Epoca')
axes[1].set_ylabel('Loss')
axes[1].legend()
axes[1].grid(True, alpha=0.3)

plt.tight_layout()
plt.savefig('curvas_entrenamiento.png', bbox_inches='tight')
plt.show()

# Diagnostico automatico
diff = hist['accuracy'][-1] - hist['val_accuracy'][-1]
print('DIAGNOSTICO:')
if diff > 0.15:
    print('OVERFITTING: diferencia train-val =', round(diff, 4))
    print('Sugerencia: mas Dropout o mas augmentation')
elif hist['accuracy'][-1] < 0.7:
    print('UNDERFITTING: accuracy baja =', round(hist['accuracy'][-1], 4))
    print('Sugerencia: mas epocas o arquitectura mas profunda')
else:
    print('Buen ajuste. El modelo aprende correctamente.')
