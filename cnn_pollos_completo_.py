"""---
##  — Métricas de Regresión vs Clasificación

En Machine Learning existen dos tipos principales de problemas y cada uno
tiene sus propias métricas de evaluación:

---

### Clasificación (nuestro proyecto)
Se usa cuando la salida es una CATEGORIA o ETIQUETA discreta.
Ejemplo: ¿Qué enfermedad tiene el pollo? → Salmonella / Coccidiosis / Healthy / NCD

| Métrica | Fórmula | Qué mide |
|---------|---------|----------|
| **Accuracy** | (TP+TN) / Total | % de predicciones correctas totales |
| **Precision** | TP / (TP+FP) | De los que predije como X, cuántos SÍ eran X |
| **Recall** | TP / (TP+FN) | De los que eran X, cuántos logré detectar |
| **F1-Score** | 2*(P*R)/(P+R) | Balance entre Precision y Recall |

Donde: TP=Verdaderos Positivos, FP=Falsos Positivos, FN=Falsos Negativos

**¿Cuándo usar F1 en vez de Accuracy?**
Cuando el dataset está desbalanceado (una clase tiene muchos más ejemplos que otras).
En nuestro caso, New Castle Disease tiene solo 562 imágenes vs 2625 de Salmonella,
por eso el F1-Score es más confiable que el Accuracy.

---

### Regresión
Se usa cuando la salida es un NÚMERO CONTINUO.
Ejemplo: ¿Cuánto pesará el pollo en 30 días? → 2.3 kg / 1.8 kg / 3.1 kg

| Métrica | Fórmula | Qué mide |
|---------|---------|----------|
| **MAE** | Media( |real - predicho| ) | Error promedio en las mismas unidades |
| **MSE** | Media( (real - predicho)² ) | Penaliza más los errores grandes |
| **RMSE** | Raíz cuadrada del MSE | Error en las mismas unidades que MSE |
| **R²** | 1 - (SS_res / SS_tot) | Qué % de la variación explica el modelo |

**¿Cuándo usar cuál?**
- MAE → cuando todos los errores son igual de importantes
- MSE/RMSE → cuando los errores grandes son muy costosos (ej: diagnóstico médico)
- R² → para saber qué tan bien el modelo explica los datos (1.0 = perfecto, 0 = no explica nada)
"""

