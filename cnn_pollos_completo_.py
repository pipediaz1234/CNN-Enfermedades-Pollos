"""---
## PASO 8 — Arquitectura de la CNN

La red tiene 4 bloques convolucionales seguidos de capas densas:

```
Entrada (224x224x3)
  Bloque 1: Conv2D(32)  + BatchNorm + MaxPool -> 112x112
  Bloque 2: Conv2D(64)  + BatchNorm + MaxPool -> 56x56
  Bloque 3: Conv2D(128) + BatchNorm + MaxPool -> 28x28
  Bloque 4: Conv2D(256) + BatchNorm + MaxPool -> 14x14
  Flatten
  Dense(512) + Dropout(0.5)
  Dense(256) + Dropout(0.3)
  Dense(4)   + Softmax
```

"""

modelo = Sequential(name='CNN_Pollos')

# Bloque 1: detecta bordes y texturas simples
modelo.add(Conv2D(32, (3,3), activation='relu', padding='same',
                  input_shape=(224, 224, 3)))
modelo.add(BatchNormalization())
modelo.add(MaxPooling2D(2, 2))

# Bloque 2: detecta esquinas y curvas
modelo.add(Conv2D(64, (3,3), activation='relu', padding='same'))
modelo.add(BatchNormalization())
modelo.add(MaxPooling2D(2, 2))

# Bloque 3: detecta formas y partes del objeto
modelo.add(Conv2D(128, (3,3), activation='relu', padding='same'))
modelo.add(BatchNormalization())
modelo.add(MaxPooling2D(2, 2))

# Bloque 4: representaciones abstractas especificas por enfermedad
modelo.add(Conv2D(256, (3,3), activation='relu', padding='same'))
modelo.add(BatchNormalization())
modelo.add(MaxPooling2D(2, 2))

# Clasificador
modelo.add(Flatten())
modelo.add(Dense(512, activation='relu'))
modelo.add(Dropout(0.5))
modelo.add(Dense(256, activation='relu'))
modelo.add(Dropout(0.3))
modelo.add(Dense(NUM_CLASES, activation='softmax'))

modelo.compile(
    optimizer=Adam(learning_rate=LR),
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

modelo.summary()
print(f'Parametros totales: {modelo.count_params():,}')

"""---
## PASO 9 — Entrenamiento

| Callback | Funcion |
|----------|---------|
| EarlyStopping | Para si val_loss no mejora en 7 epocas |
| ReduceLROnPlateau | Reduce LR a la mitad si no mejora en 3 epocas |
| ModelCheckpoint | Guarda el mejor modelo automaticamente |

"""

callbacks = [
    EarlyStopping(
        monitor='val_loss', patience=7,
        restore_best_weights=True, verbose=1
    ),
    ReduceLROnPlateau(
        monitor='val_loss', factor=0.5,
        patience=3, min_lr=1e-6, verbose=1
    ),
    ModelCheckpoint(
        filepath='mejor_modelo.h5',
        monitor='val_accuracy',
        save_best_only=True, verbose=1
    )
]

print('Iniciando entrenamiento...')
print(f'Train: {len(df_train)} imagenes | Val: {len(df_val)} imagenes')
print('='*60)

historia = modelo.fit(
    train_gen,
    epochs=EPOCHS,
    validation_data=val_gen,
    callbacks=callbacks,
    verbose=1
)

mejor_acc   = max(historia.history['val_accuracy'])
epocas_reales = len(historia.history['accuracy'])
print()
print('='*60)
print('ENTRENAMIENTO FINALIZADO')
print(f'Epocas ejecutadas:  {epocas_reales}')
print(f'Mejor val_accuracy: {mejor_acc:.4f} ({mejor_acc*100:.2f}%)')

"""---
## PASO 10 — Curvas de entrenamiento

- **Buen ajuste:** train y val suben juntas
- **Overfitting:** train sube pero val se estanca o baja
- **Underfitting:** ambas son bajas

"""

hist   = historia.history
epocas = range(1, len(hist['accuracy']) + 1)

fig, axes = plt.subplots(1, 2, figsize=(14, 5))
fig.suptitle('Curvas de Entrenamiento', fontsize=14, fontweight='bold')

axes[0].plot(epocas, hist['accuracy'],     'b-o', label='Train', markersize=4)
